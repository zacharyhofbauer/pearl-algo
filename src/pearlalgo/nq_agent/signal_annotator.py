class LLMSignalAnnotator:
    """
    LLM-powered signal annotator for trading signals.
    
    Uses Claude to analyze signals and provide human-readable explanations.
    All operations are non-blocking and fail gracefully.
    
    Configuration:
    - enabled: Master toggle for annotations
    - model: Claude model to use (default: claude-sonnet-4-20250514)
    - timeout_seconds: Max time to wait for annotation (default: 5s)
    - batch_mode: Whether to batch multiple signals (default: False)
    """
    
    def __init__(
        self,
        enabled: bool = True,
        model: str = "claude-sonnet-4-20250514",
        timeout_seconds: float = 5.0,
        batch_mode: bool = False,
    ):
        """
        Initialize the signal annotator.
        
        Args:
            enabled: Whether annotation is enabled
            model: Claude model to use
            timeout_seconds: Timeout for annotation requests
            batch_mode: Whether to batch signals (not yet implemented)
        """
        self.enabled = enabled
        self.model = model
        self.timeout_seconds = timeout_seconds
        self.batch_mode = batch_mode
        
        # Initialize Claude client (may be None if not configured)
        self._client: Optional[ClaudeClient] = None
        if enabled and OPENAI_AVAILABLE:
            try:
                self._client = get_claude_client()
            except Exception as e:
                logger.warning(f"Failed to initialize Claude client for annotations: {e}")
        
        # Stats
        self._total_requests = 0
        self._successful_requests = 0
        self._total_latency_ms = 0
        
        status = "enabled" if self._client else "disabled (client unavailable)"
        logger.info(f"LLMSignalAnnotator initialized: {status}, model={model}, timeout={timeout_seconds}s")
    
    @property
    def is_available(self) -> bool:
        """Check if annotation is available."""
        return self.enabled and self._client is not None
    
    def annotate_signal(
        self,
        signal: Dict,
        market_context: Optional[Dict] = None,
        performance_metrics: Optional[Dict] = None,
    ) -> SignalAnnotation:
        """
        Annotate a trading signal synchronously.
        
        This is a blocking call - use annotate_signal_async for non-blocking.
        
        Args:
            signal: Signal dictionary from signal generator
            market_context: Optional additional market context
            performance_metrics: Optional recent performance metrics
            
        Returns:
            SignalAnnotation with analysis results
        """
        if not self.is_available:
            return SignalAnnotation.from_error("Annotator not available")
        
        start_time = datetime.now(timezone.utc)
        self._total_requests += 1
        
        try:
            # Build prompt
            user_prompt = self._build_prompt(signal, market_context, performance_metrics)
            
            # Call Claude
            response = self._client.chat(
                messages=[{"role": "user", "content": user_prompt}],
                system_prompt=SIGNAL_ANNOTATION_SYSTEM_PROMPT,
            )
            
            # Parse response
            annotation = self._parse_response(response)
            
            # Calculate latency
            latency_ms = int((datetime.now(timezone.utc) - start_time).total_seconds() * 1000)
            annotation.latency_ms = latency_ms
            annotation.model = self.model
            
            self._successful_requests += 1
            self._total_latency_ms += latency_ms
            
            logger.debug(f"Signal annotation completed in {latency_ms}ms")
            return annotation
            
        except Exception as e:
            logger.warning(f"Signal annotation failed: {e}")
            return SignalAnnotation.from_error(str(e))
    
    async def annotate_signal_async(
        self,
        signal: Dict,
        market_context: Optional[Dict] = None,
        performance_metrics: Optional[Dict] = None,
    ) -> SignalAnnotation:
        """
        Annotate a trading signal asynchronously with timeout.
        
        This is the recommended method for use in the signal pipeline.
        
        Args:
            signal: Signal dictionary from signal generator
            market_context: Optional additional market context
            performance_metrics: Optional recent performance metrics
            
        Returns:
            SignalAnnotation with analysis results (or error annotation on timeout)
        """
        if not self.is_available:
            return SignalAnnotation.from_error("Annotator not available")
        
        try:
            # Run sync annotation in thread pool with timeout
            loop = asyncio.get_event_loop()
            result = await asyncio.wait_for(
                loop.run_in_executor(
                    None,
                    self.annotate_signal,
                    signal,
                    market_context,
                    performance_metrics,
                ),
                timeout=self.timeout_seconds,
            )
            return result
            
        except asyncio.TimeoutError:
            logger.debug(f"Signal annotation timed out after {self.timeout_seconds}s")
            return SignalAnnotation.from_error(f"Timeout ({self.timeout_seconds}s)")
        except Exception as e:
            logger.warning(f"Async annotation failed: {e}")
            return SignalAnnotation.from_error(str(e))
    
    def _build_prompt(
        self,
        signal: Dict,
        market_context: Optional[Dict],
        performance_metrics: Optional[Dict],
    ) -> str:
        """Build the user prompt for signal annotation."""
        # Extract signal data
        signal_type = signal.get("type", "unknown")
        direction = signal.get("direction", "unknown")
        confidence = signal.get("confidence", 0.0)
        entry_price = signal.get("entry_price", 0.0)
        stop_loss = signal.get("stop_loss", 0.0)
        take_profit = signal.get("take_profit", 0.0)
        
        # Calculate risk/reward
        if direction == "long" and entry_price > 0 and stop_loss > 0:
            risk = entry_price - stop_loss
            reward = take_profit - entry_price if take_profit else 0
            risk_reward = reward / risk if risk > 0 else 0
        elif direction == "short" and entry_price > 0 and stop_loss > 0:
            risk = stop_loss - entry_price
            reward = entry_price - take_profit if take_profit else 0
            risk_reward = reward / risk if risk > 0 else 0
        else:
            risk_reward = 0
        
        # Extract regime data
        regime = signal.get("regime", {})
        regime_type = regime.get("regime", "unknown")
        volatility = regime.get("volatility", "normal")
        session = regime.get("session", "unknown")
        
        # Extract indicators
        indicators = signal.get("indicators", {})
        rsi = indicators.get("rsi", 50.0) or 50.0
        macd_hist = indicators.get("macd_histogram", 0.0) or 0.0
        volume_ratio = indicators.get("volume_ratio", 1.0) or 1.0
        atr = indicators.get("atr", 0.0) or 0.0
        atr_pct = atr / entry_price if entry_price > 0 else 0
        
        # Format custom indicators
        custom_features = signal.get("custom_features", {})
        indicator_metadata = signal.get("indicator_metadata", {})
        custom_lines = []
        
        # Add indicator metadata (from custom indicator signals)
        if indicator_metadata:
            for key, value in indicator_metadata.items():
                if isinstance(value, float):
                    custom_lines.append(f"  - {key}: {value:.3f}")
                else:
                    custom_lines.append(f"  - {key}: {value}")
        
        # Add key custom features (limit to most relevant)
        if custom_features:
            # Prioritize zone-related features
            priority_keys = [
                "sd_in_demand_zone", "sd_in_supply_zone", "sd_zone_strength",
                "pc_trend_bullish", "pc_trend_bearish", "pc_channel_position",
                "smd_bullish_divergence", "smd_bearish_divergence",
            ]
            for key in priority_keys:
                if key in custom_features:
                    value = custom_features[key]
                    if isinstance(value, float) and value > 0:
                        custom_lines.append(f"  - {key}: {value:.2f}")
        
        custom_indicators = "\n".join(custom_lines) if custom_lines else "  (none)"
        
        # Extract performance metrics
        if performance_metrics:
            recent_wr = performance_metrics.get("win_rate", 0.5) * 100
            recent_trades = performance_metrics.get("total_trades", 0)
        else:
            recent_wr = 50.0
            recent_trades = 0
        
        # Build prompt
        return SIGNAL_ANNOTATION_USER_TEMPLATE.format(
            signal_type=signal_type,
            direction=direction,
            confidence=confidence,
            entry_price=entry_price,
            stop_loss=stop_loss,
            take_profit=take_profit,
            risk_reward=risk_reward,
            regime_type=regime_type,
            volatility=volatility,
            session=session,
            rsi=rsi,
            macd_hist=macd_hist,
            volume_ratio=volume_ratio,
            atr=atr,
            atr_pct=atr_pct,
            custom_indicators=custom_indicators,
            recent_wr=recent_wr,
            recent_trades=recent_trades,
        )
    
    def _parse_response(self, response: str) -> SignalAnnotation:
        """Parse Claude's JSON response into SignalAnnotation."""
        try:
            # Try to extract JSON from response
            response = response.strip()
            
            # Handle markdown code blocks
            if response.startswith("```"):
                lines = response.split("\n")
                response = "\n".join(lines[1:-1] if lines[-1].startswith("```") else lines[1:])
            
            # Parse JSON
            data = json.loads(response)
            
            return SignalAnnotation(
                explanation=str(data.get("explanation", "")),
                key_factors=data.get("key_factors", [])[:3],
                risks=data.get("risks", [])[:2],
                confidence_note=str(data.get("confidence_note", "Medium confidence")),
                summary_emoji=str(data.get("summary_emoji", "🟡")),
            )
            
        except json.JSONDecodeError as e:
            logger.warning(f"Failed to parse annotation response: {e}")
            # Try to extract useful info from non-JSON response
            if response:
                return SignalAnnotation(
                    explanation=response[:200] if len(response) > 200 else response,
                    confidence_note="Analysis available",
                    summary_emoji="🟡",
                )
            return SignalAnnotation.from_error("Invalid response format")
    
    def get_stats(self) -> Dict[str, Any]:
        """Get annotation statistics."""
        avg_latency = (
            self._total_latency_ms / self._successful_requests
            if self._successful_requests > 0
            else 0
        )
        success_rate = (
            self._successful_requests / self._total_requests
            if self._total_requests > 0
            else 0
        )
        
        return {
            "total_requests": self._total_requests,
            "successful_requests": self._successful_requests,
            "success_rate": success_rate,
            "avg_latency_ms": avg_latency,
            "is_available": self.is_available,
            "model": self.model,
        }


