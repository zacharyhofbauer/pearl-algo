@dataclass
class SignalAnnotation:
    """Annotation result from LLM analysis."""
    explanation: str = ""
    key_factors: List[str] = field(default_factory=list)
    risks: List[str] = field(default_factory=list)
    confidence_note: str = "Medium confidence"
    summary_emoji: str = "🟡"
    
    # Metadata
    model: str = ""
    latency_ms: int = 0
    error: Optional[str] = None
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for serialization."""
        return {
            "explanation": self.explanation,
            "key_factors": self.key_factors,
            "risks": self.risks,
            "confidence_note": self.confidence_note,
            "summary_emoji": self.summary_emoji,
            "model": self.model,
            "latency_ms": self.latency_ms,
            "error": self.error,
        }
    
    def format_telegram(self) -> str:
        """Format annotation for Telegram message."""
        if self.error:
            return ""
        
        lines = []
        
        # Summary
        lines.append(f"{self.summary_emoji} *AI Analysis:* {self.explanation}")
        
        # Key factors
        if self.key_factors:
            lines.append("\n*Key Factors:*")
            for factor in self.key_factors[:3]:
                lines.append(f"  • {factor}")
        
        # Risks
        if self.risks:
            lines.append("\n*Risks:*")
            for risk in self.risks[:2]:
                lines.append(f"  ⚠️ {risk}")
        
        # Confidence
        lines.append(f"\n*Assessment:* {self.confidence_note}")
        
        return "\n".join(lines)
    
    @classmethod
    def from_error(cls, error: str) -> "SignalAnnotation":
        """Create annotation from error."""
        return cls(
            explanation="",
            error=error,
        )


